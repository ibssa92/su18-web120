<p>I will be diving into a discussion that commonly takes place in the world of Web development; Responsive Websites versus Native Mobile Apps. Within my research I will present pros and cons for both sides as well as including my own thoughts and opinions as to which one I prefer. I will start with Responsive websites.
</p> 
 
<p>With a responsive website, you are able to create a single website that will be able to fit into any device screen that you view the website on. The way this is done is the developer will code the website for when screen resolution changes, the websites display will act accordingly. As you would imagine, some of the pros in regards to that is you will only need to create and maintain ONE website, that’s it. As a result, another pro is cost efficiency. In the long run, it will be cheaper if you are just maintaining a single website. According to Jason Summerfield, “..website development is considerably more time and cost-effective. (Summerfield)”. However, one con to this is user experience. Having a responsive website gives your mobile users and desktop users the same experience, which is definitely not a good thing. Different devices should always call on different experiences.</p>

<p>
    Mobile apps are software which are created specifically for mobile devices. Some mobile apps we are all familiar with are the Instagram App, Snapchat App, Facebook App, etc. The greatest pro for mobile apps are the user experience. The design and interface of mobile apps are made to allow the best possible experience for the users. Another pro is the easy accessibility. Once you download an app, you can just access it with the click of a button whereas with a website, you have to go to your browser and type in the URL. However, with mobile apps comes higher costs to maintain it. Also, mobile apps are built to work on a specific operating system and will not work on a non-compatible operating system. <em>“Native apps are not common among all devices, because they are made for a particular operating system. (Ghazaryan)”</em>
</p>

<p>
My stance on this is that if you can afford it, always build a mobile app. In the long run, it will make your users happy. In my opinion, the ultimate goal is to create the best user experience possible, so the user should always be in mind when you are deciding whether to go with a responsive site or mobile app.
</p>
