<p>Prior to taking this class, I had no idea what flexbox was all about. Now that I am learning about it, I can see why it is needed in CSS and Web Developing as a whole. Flexbox is a new layout mode in CSS. Instead of having your typical complex float layouts, flexbox layouts allow you to create complex layouts into efficient coding. Previously, there were only 4 layout modes; <strong>block, inline, table, and positioned.</strong> With the arrival of CSS3 came 2 new layouts; grid and flexbox. According to Scott Vandehey, “Flexbox layout distributes space along a single column or row. Like float layouts, but better! The Flexbox spec is finished and enjoys excellent browser support today. (Vandehey).” As you would imagine, many browsers have now implemented Flexbox to their framework due to its rapidly growing popularity. Flexbox is fully supported in all the modern browsers.
</p> 
 
<p>The way Flexbox works is that it takes your elements and lays them out in a single row or column. So you have a Flex container, and within your flex container you will have flex items. The items in your flex container can be laid out either horizontally or vertically. Everything you do in flexbox refers back to your 2 axis’s; the main axis and the cross axis. Also, with flexbox comes a whole new set of properties, such as the flex-grow property, flex-basis property, etc. With the flexbox layout, you have so many options and different ways you can style your content.</p>

<p>
In conclusion, Flexbox is a new layout mode in CSS3 which allows you to build very complex layouts on your site yet makes it easy on you to create and manage since it takes a minimal amount of coding. It is supported on almost all major browsers so using it should be no issue.
</p>
